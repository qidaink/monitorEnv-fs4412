int main(int argc, const char *argv[])
{
	/* 0.相关变量定义 */
	int fd = -1; /* 文件描述符 */
	buzzer_desc_t buzzer;
	int i = 0;
	/* 1.判断参数个数是否合法 */
	/* 1.1判断参数数量 */
	if (argc < 2)
	{
		printf("\nusage:\n");
		printf("%s /dev/dev_name\n", argv[0]);
		return -1;
	}
	/* 2.打开字符设备 */
	if ((fd = open(argv[1], O_RDWR | O_NONBLOCK)) < 0) /* 默认是阻塞的 */
	{
		printf("[ERROR]open %s failed!\n", argv[1]);
		return -1;
	}
	/* 3.蜂鸣器控制测试 */
	ioctl(fd, BUZZER_ON);/* 打开蜂鸣器 */

	for (i = 0; i < sizeof(MUSIC) / sizeof(MUSIC[0]); i += 2)
	{
		buzzer.tcnt = MUSIC[i];
		buzzer.tcmp = MUSIC[i] / 2;
		ioctl(fd, BUZZER_FREQ, &buzzer);
		usleep(MUSIC[i + 1] * 20000);
		if(i == 10)
			ioctl(fd, BUZZER_OFF);
		if(i == 20)
			ioctl(fd, BUZZER_ON);
	}

	printf("[INFO ]music play over....\n");
	/* 4.关闭文件描述符 */
	close(fd);
	fd = -1;
	return 0;
}
